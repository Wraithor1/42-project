int	ft_strcmp(const char *s1, const char *s2)
{
	int i;
	int result;

	i = 0;
	result = 0;
	while (s1[i] != '\0')
	{
		if (s1[i] - s2[i] != 0)
		{
			result = s1[i] - s2[i];
			break ;
		}
		i++;
	}
	if (s1[i] == '\0' && s2[i] != '\0')
		result = s1[i] - s2[i];
	return (result);
}
