#include <string.h>

void	*ft_memset(void *str, int c, size_t n)
{
	int		i;
	char	*ptr;

	i = 0;
	while (i < n)
	{
		ptr = str + i;
		*ptr = c;
		i++;
	}
	return (str);
}
