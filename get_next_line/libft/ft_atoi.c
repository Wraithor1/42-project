int	ft_atoi(const char *str)
{
	int i;
	int result;

	i = 0;
	result = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= '0' && str[i] <= '9')
		{
			result *= 10;
			result += str[i] - 48;
		}
		else
			break ;
		i++;
	}
	return (result);
}
